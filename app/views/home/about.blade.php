<h2>简介</h2>
<p>统计之都（Capital of Statistics，简称COS）成立于2006年5月，是一个旨在推广与应用统计学知识的网站和社区。统计之都网站最初由谢益辉创办，现由世界各地的众多志愿者共同管理维护。</p>
<p>伴随着统计学的迅速发展，以及当前数据科学的持续升温，我们越来越能体会到统计学及其相关技术在应用领域的巨大潜力——现代管理、咨询、商业、经济、金融、医药、生物等等，无不需要数据的力量；而另一方面我们也同样意识到，统计学的理论与应用之间还存在着不小的间隔——无论是实际应用中人们对统计学基础理论的欠缺和误解，还是理论研究中研究者对应用领域的忽视。</p>
<p>统计之都便是基于这样的认识而创建的。我们希望统计理论研究者能充分关注应用问题，而统计应用者也能正确把握统计学基本知识，将统计学这门应用学科真正的潜力开发出来。同时，借着数据科学这股东风，我们希望搭建一个开放的平台，使得科研人员、数据分析人员和统计学爱好者能互相交流合作，一方面促进彼此知识和技能的增长，另一方面为国内的统计学和数据科学贡献自己的力量。</p>
<p>统计之都为非营利性质网站，但大力欢迎所有应用和研究领域的朋友与我们在实际应用问题上合作。</p>
<h2>愿景目标</h2>
<p>我们的口号是：</p>
<h3 style="text-align: center;"><span style="color: #cc0000;">中国统计学门户网站，免费统计学服务平台</span></h3>
<p>我们怀着“十年磨一剑”的决心，要将统计之都创建成中国的统计学门户网站；我们抱着“己欲立而立人、己欲达而达人”的信条，要将统计之都以免费统计学服务平台的形式坚持办下去。我们希望统计之都在专业知识体系上有真正的王者风范，在面对用户需求时却又以谦恭的态度为大家服务。</p>
