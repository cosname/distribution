<h2>网站结构</h2>
<p>本站的组织形式为：<a title="COS主站" href="http://cos.name">主站</a> + <a title="COS论坛" href="http://cos.name/cn">中文论坛</a>。</p>
<ul>
<li>主站以博客的形式创建，内容以统计学知识介绍和应用案例为主，中间附带发布本站新闻和通知，本站同时支持国内高校统计课程的建设（设有“高校课堂”栏目），间或发表书评、文评以及其他统计学网站或博客导读；主站文章来源于本站撰稿人的贡献以及用户推荐，我们对这些文章有一定的审稿机制以保证文章质量。
<ul>
<li>主站目标：在专业水准上超越纸质期刊，力争每一篇文章都能让读者真正受益。</li>
<li>稿件的标准：重实用、有新意、无抄袭、可重现（读者可自行重现所有统计结果，即reproducible）。</li>
</ul>
</li>
<li>论坛以针对统计学主题的广泛讨论为主，形式比较随意，任何用户都可注册。
<ul>
<li>论坛目标：以最高效的方式解答疑问，传播知识，做最专业的统计学论坛。</li>
<li>我们希望提供给用户最大的便利性，甚至不用注册和发帖就能寻找到答案。</li>
</ul>
</li>
</ul>
<h2>线下活动</h2>
<p>除了线上的交流之外，统计之都社区还定期开展一系列的线下活动，其中最重要的是自 2008 年以来一直延续至今的<a href="http://cos.name/chinar/">中国 R 语言会议</a>，每年分别于 5 月和 11 月在北京和上海召开。</p>
<p>自 2012 年以来，统计之都还开展了一系列的<a href="http://cos.name/tag/salon/">数据分析沙龙</a>，其主题涵盖了可视化、生物信息、金融、空间统计、海量数据处理等众多前沿的统计学问题。</p>
